func TestFileFileName(t *testing.T) {
	tests := []struct {
		subject string
		want    string
	}{
		{`[01/66] - "nova.s44e02.720p-dhd.r00" yEnc (01/66)`, "nova.s44e02.720p-dhd.r00"},
		{`"simple.nfo" yEnc (1/1)`, "simple.nfo"},
		{`no quotes here`, ""},
		{`"unclosed`, ""},
		{`[1/1] - "release.srr" yEnc (1/1)`, "release.srr"},
	}
	for _, tc := range tests {
		f := File{Subject: tc.subject}
		got := f.FileName()
		if got != tc.want {
			t.Errorf("FileName(%q) = %q, want %q", tc.subject, got, tc.want)
		}
	}
}

func TestNZBSRRFile(t *testing.T) {
	nzbXML := `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE nzb PUBLIC "-//newzBin//DTD NZB 1.0//EN" "http://www.newzbin.com/DTD/nzb/nzb-1.0.dtd">
<nzb xmlns="urn:newzbin:nzb:1.0">
  <file poster="test" date="1234567890" subject="[1/3] - &quot;nova.s44e02.720p-dhd.srr&quot; yEnc (1/1)">
    <groups><group>alt.binaries.test</group></groups>
    <segments><segment bytes="100" number="1">abc@def</segment></segments>
  </file>
  <file poster="test" date="1234567890" subject="[2/3] - &quot;nova.s44e02.720p-dhd.r00&quot; yEnc (1/66)">
    <groups><group>alt.binaries.test</group></groups>
    <segments><segment bytes="100" number="1">ghi@jkl</segment></segments>
  </file>
</nzb>`
	n, err := Parse(strings.NewReader(nzbXML))
	if err != nil {
		t.Fatalf("Parse: %v", err)
	}
	f := n.SRRFile()
	if f == nil {
		t.Fatal("SRRFile() returned nil, want non-nil")
	}
	name := f.FileName()
	if !strings.HasSuffix(strings.ToLower(name), ".srr") {
		t.Errorf("SRRFile().FileName() = %q, want .srr suffix", name)
	}
}

